#include "libft.h"

size_t	ft_wc(const char *str, char c)
{
	size_t	count;

	count = 0;
	while (str && *str)
	{
		if (*str != c)
		{
			while (*str && *str != c)
				str++;
			count++;
		}
		if (!(*str))
			break ;
		str++;
	}
	return (count);
}
